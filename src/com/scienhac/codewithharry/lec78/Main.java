/*
We have three types of error
1. Compile Time Error or Syntax Error
2. Logical Error --> produces wrong output or bug
3. Runtime Error --> occurs during execution of program
   e.g. dividing a number by zero, invalid type casting, accessing invalid index of array etc.

   In Java, Runtime Errors are represented by Exceptions and Errors.
   Exceptions are further divided into Checked Exceptions and Unchecked Exceptions.
   Checked Exceptions are checked at compile time, e.g. IOException, SQLException etc.
   Unchecked Exceptions are not checked at compile time, e.g. ArithmeticException, NullPointerException etc.

   Errors are serious problems that a reasonable application should not try to catch.
   e.g. OutOfMemoryError, StackOverflowError etc.


 */

